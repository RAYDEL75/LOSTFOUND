     void cargar(){
    String [] titulos = {"Nombre","Apellidos","Correo","Telefono","Objeto","Descripcion","Fecha","Estado"};
    String [] registros = new String[8];
    
    String sql = "SELECT * FROM objetos";
    
    model = new DefaultTableModel(null,titulos);
    
    conectar cc = new conectar();
    Connection cn = cc.conexion();
        try {
          Statement  st = cn.createStatement();
          ResultSet rs =st.executeQuery(sql);
          
          while(rs.next()){
          registros[0] = rs.getString("nom");
          registros[1] = rs.getString("ape");
          registros[2] = rs.getString("cor");
          registros[3] = rs.getString("tel");
          registros[4] = rs.getString("obj");
          registros[5] = rs.getString("des");
          registros[6] = rs.getString("fecha");
          registros[7] = rs.getString("estado");
          model.addRow(registros);
          }
          t_datos.setModel(model);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    
