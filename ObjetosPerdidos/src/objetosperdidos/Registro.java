    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        // TODO add your handling code here:
        conectar cc = new conectar();
            Connection cn = cc.conexion();
            String nom,ape,cor,tel,obj,des,fec,est;
            String sql="";
            nom = t_nom.getText();
            ape = t_ape.getText();
            cor = t_cor.getText();
            tel = t_tel.getText();
            obj = t_obj.getText();
            des = t_des.getText();
            fec = t_fec.getText();
            est = t_est.getText();
          
            sql="INSERT INTO objetos(nom,ape,cor,tel,obj,des,fecha,estado) VALUES(?,?,?,?,?,?,?,?)";
            try {
            PreparedStatement pst = cn.prepareStatement(sql);
            pst.setString(1,nom);
            pst.setString(2,ape);
            pst.setString(3,cor);
            pst.setString(4,tel);
            pst.setString(5,obj);
            pst.setString(6,des);
            pst.setString(7,fec);
            pst.setString(8,est);
            
            int n = pst.executeUpdate();
            if(n>0){
            JOptionPane.showMessageDialog(null,"Registrado guardado con exito");
            bloquear();
            cargar();
            }
        } catch (SQLException ex) {
            Logger.getLogger(Registro.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_guardarActionPerformed

